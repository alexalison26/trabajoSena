<?php
function notas($pnota, $pporcent) {
    $nota = $pnota;
    $porcent = $pporcent;
    $result = $nota * $porcent;
    return $result;
}

function notasExp ($pnota, $pporcent) {
    $nota = $pnota;
    $porcent = $pporcent;
    $result = $nota * $porcent;
    return $result;
}
