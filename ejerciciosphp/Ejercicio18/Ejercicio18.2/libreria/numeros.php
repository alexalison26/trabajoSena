function numeros($pnumFin) {
    $numFin = $pnumFin;
    '$result';
    for ($cont = 1; $cont <= $numFin; $cont++) {
        $result = $numFin * $cont;
        var_dump(`$numFin * $cont = $result`);
    }
    return 'Fin';
}

