function promedios($pnota1, $pnota2, $pnota3) {
    $nota1 = $pnota1;
    $nota2 = $pnota2;
    $nota3 = $pnota3;
    $promedio = ($nota1 + $nota2 + $nota3) / 3;
    return $promedio;
}

