function numeros(pnum1, pnum2) {
    let num1 = pnum1;
    let num2 = pnum2;
    if (num1 == num2) {
        return 'Son iguales';
    } else if (num1 > num2) {
        return `${num1} es mayor`;
    } else {
        return `${num2} es mayor`;
    }
}

