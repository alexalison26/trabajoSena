function suma(pnum1, pnum2) {
    let num1 = pnum1;
    let num2 = pnum2;
    let sumar;
    sumar = num1 + num2;
    return sumar;
}

function resta(pnum1, pnum2) {
    let num1 = pnum1;
    let num2 = pnum2;
    let restar;
    restar = num1 - num2;
    return restar;
}

function multiplicacion(pnum1, pnum2) {
    let num1 = pnum1;
    let num2 = pnum2;
    let multiplicar;
    multiplicar = num1 * num2;
    return multiplicar;
}

function division(pnum1, pnum2) {
    let num1 = pnum1;
    let num2 = pnum2;
    let dividir;
    dividir = num1 / num2;
    return dividir;
}